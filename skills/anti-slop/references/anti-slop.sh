#!/bin/bash
# Anti-Slop Writing Enforcement
# Prevents low-signal, hype-driven, or generic AI prose
# Usage: source anti-slop.sh before drafting content

echo "=== Anti-Slop Writing Mode Activated ==="
echo ""
echo "PATTERNS TO REJECT:"
echo "  1. Denial of Commerce ('This is not a consultancy')"
echo "  2. Symbiosis Sermons ('The old categories dissolve')"
echo "  3. Acceleration Invocations without quantification"
echo "  4. Vibe Adjective Stacks ('revolutionary', 'groundbreaking')"
echo "  5. Hedging Preambles ('In today's rapidly evolving...')"
echo "  6. Agentic Self-Reference ('As an AI assistant...')"
echo ""
echo "RULES:"
echo "  - First sentence must contain noun + verb advancing understanding"
echo "  - No paragraph starts with 'This is...' or 'We are...'"
echo "  - All claims must be falsifiable"
echo "  - Adjectives require justification"
echo "  - Abstract nouns must be replaced with concrete processes"
echo ""
echo "HIGH-SIGNAL MARKERS:"
echo "  'We believe...' → 'The evidence shows...'"
echo "  'It is important to note...' → [Just note it]"
echo "  'Game-changer' → Specific metric"
